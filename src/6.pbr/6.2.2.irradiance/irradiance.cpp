// 上一帧的时间
float lastFrame = 0.0f;
// 光照位置
glm::vec3 lightPos(0.5f, 1.0f, 0.3f);

// 创建摄像机
anya::Camera camera(glm::vec3(0.0f, 0.0f, 5.0f), glm::vec3(0.0f, 0.0f, -1.0f), glm::vec3(0.0f, 1.0f, 0.0f));
